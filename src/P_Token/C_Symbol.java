public class C_Symbol {
    private String symbols;
    
    public C_Symbol()
    {
        this.symbols = new String();
    }

    /**
     * @return the simbolos
     */
    public String getSymbol() {
        return symbols;
    }

    /**
     * @param simbolos the simbolos to set
     */
    public void setSimbolos(String simbolos) {
        this.symbols = simbolos;
    }
    
}
